<template>

		<div ref="board" class="board">

		</div>

</template>

<style>

	.board {
		width:100%;
		height: 100%;
		padding-top:100%; /* 1:1 aspect */
	}

	.cg-wrap {
		border-radius: 3px;
	}

</style>

<script>

	import {Chessground} from 'chessground';

	import 'chessground/assets/chessground.base.css';
	import 'chessground/assets/chessground.brown.css';
	//import 'chessground/assets/chessground.cburnett.css';

	import '@/styles/pieces/staunty.css';

	import { Chess, SQUARES } from 'chess.js';

	export default {
		name: "board",

		props: ['fen'],

		data() {
			return {
				game: null,
				board: null,
				observer: null,
				rules: true,
				history: [],
			}
		},

		created () {
			this.game = new Chess();
			this.game.load("");
		},

		mounted() {

			this.board = Chessground(this.$refs.board, {
				animation: {
					enabled: true,
				},
				movable: {
					dests: this.possibleMoves(),
					showDests: true,
				}
			});
			
			//this.board.set(config);
			
			window.addEventListener('resize', this.onResize);
			//this.observer = new ResizeObserver(() => this.onResize(), this.$refs.boardwrapper);

			this.onResize();

			this.loadPosition();

		},

		unmounted() {
			window.removeEventListener('resize', this.onResize);
		},

		watch: {
			 fen: function (newFen) {
				this.fen = newFen
				this.loadPosition()
			},
		},

		methods: {
			// https://github.com/vitogit/vue-chessboard/blob/master/src/components/chessboard/index.vue
			possibleMoves () {
				const dests = {
					get: function(value) { return this[value]; } // this is some kind of bug
				}
				SQUARES.forEach(s => {
					const ms = this.game.moves({square: s, verbose: true})
					if (ms.length) dests[s] = ms.map(m => m.to)
				})
				return dests;
			},

			toColor () {
				return (this.game.turn() === 'w') ? 'white' : 'black'
			},

			onMoved() {
				return (orig, dest, metadata) => {
					var move = this.game.move({from: orig, to: dest})

					if (this.rules) this.board.set({fen: this.game.fen()});

					this.board.set({
						turnColor: this.toColor(),
						movable: {
							color: this.rules ? this.toColor() : 'both',
							dests: this.possibleMoves(),
						},
					});

					if (!move && this.rules) return;

					this.history.push(dest);

					this.$emit('onMove', {
						history: this.history, 
						fen: this.rules ? this.game.fen() : this.board.getFen(),
					});
				}
			},

			// load positional data for both Chess.js and Chessground
			loadPosition() {
				this.game.load(this.fen);
				 this.board.set({
					fen: this.game.fen(),
					turnColor: this.toColor(),
					movable: {
						color: this.rules ? this.toColor() : 'both',
						dests: this.possibleMoves(),
					},
					orientation: 'white',
				});

				this.board.set({
					movable: { events: { after: this.onMoved() } },
				});
			},

			setState(state) {
				this.board.set(state);
			},

			setRulesEnabled(enabled) {

				this.rules = enabled;

				this.board.set({
					movable: {
						color: enabled ? this.toColor() : 'both',
						free: !enabled,
					}
				});

				if (enabled) this.game.load(this.board.getFen());

			},

			onResize() {
				this.board.redrawAll();
			},

		}
	};
</script>
